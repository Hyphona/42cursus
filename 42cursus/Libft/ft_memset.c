#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int		i;
	char	*p_s;

	i = 0;
	p_s = (char *) s;
	while (i < (int) n)
	{
		p_s[i] = (char) c;
		i++;
	}
	return (s);
}
