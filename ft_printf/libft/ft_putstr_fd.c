#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	size_t	i;
	size_t	len;

	if (!s || !fd)
		return (0);
	i = 0;
	len = ft_strlen(s);
	while (i < len)
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
	return (len);
}
