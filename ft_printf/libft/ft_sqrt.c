int	ft_sqrt(int nb)
{
	int	i;
	int	x;

	i = 0;
	if (nb < 0)
		return (0);
	while (i <= nb && i < 46341)
	{
		x = i * i;
		if (x == nb)
			return (i);
		if (nb < x)
			return (0);
		i++;
	}
	return (0);
}
