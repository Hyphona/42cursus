#include "libft.h"

size_t	ft_countword(char const *s, char c)
{
	size_t	i;
	size_t	word;

	i = 0;
	word = 0;
	while (s[i])
	{
		if ((i == 0 && s[i] != c) || (s[i] != c && s[i - 1] == c))
			word++;
		i++;
	}
	return (word);
}
