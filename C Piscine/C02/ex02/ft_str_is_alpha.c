int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i])
	{
		if (str[i] > 'Z' && str[i] < 'a')
			return (0);
		else if (str[i] > 'z' || str[i] < 'A')
			return (0);
		i++;
	}
	return (1);
}
